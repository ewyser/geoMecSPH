function CPAplast!(τ,ϵ,epII,coh,phi,nmp,Del,Hp,cr)
    ψ    = 0.5*pi/180.0
    ftol = 1e-6
    ηtol = 1e4
    @threads for p in 1:nmp
        ϕ   = phi[p]
        H   = cos(ϕ)*Hp
        ϵII0= epII[p]
        c0  = coh[p]+Hp*ϵII0
        if c0<cr
            c0 = cr
        end
        σm  = 0.5*(τ[1,p]+τ[2,p])
        τII = sqrt(0.25*(τ[1,p]-τ[2,p])^2+τ[4,p]^2)
        f   = τII+σm*sin(ϕ)-c0*cos(ϕ)
        e   = [0 f]
        if f>0.0
            ϵII = ϵII0
            Δϵ  = zeros(Float64,4,1)
            η   = 0
            while abs(f)>ftol
                η  += 1
                ∂σf = [ (τ[1,p]-τ[2,p])/(4*τII)+sin(ϕ)/2;
                       -(τ[1,p]-τ[2,p])/(4*τII)+sin(ϕ)/2;
                        0.0                             ;
                        τ[4,p]/τII                      ]
                ∂σg = [ (τ[1,p]-τ[2,p])/(4*τII)+sin(ψ)/2;
                       -(τ[1,p]-τ[2,p])/(4*τII)+sin(ψ)/2;
                        0.0                             ;
                        τ[4,p]/τII                      ] 

                Δγ  = f/(H+∂σf'*Del*∂σg)
                Δσ  = Δγ*Del*∂σg
                Δϵ  = Δϵ+Del\Δσ
                τ[:,p] -= Δσ
                ϵII = ϵII0+sqrt(2/3*(Δϵ[1]^2+Δϵ[2]^2+Δϵ[3]^2+2*Δϵ[4]^2))
                c0  = coh[p]+Hp*ϵII
                if c0<cr
                    c0 = cr
                end
                σm = 0.5*(τ[1,p]+τ[2,p])
                τII = sqrt(0.25*(τ[1,p]-τ[2,p])^2+τ[4,p]^2)
                f = τII+σm*sin(ϕ)-c0*cos(ϕ)
                if η>ηtol
                    @printf("\nCPA: max(η_it)>%d",ηtol)
                    @printf("\n     f = %.6f",f)
                    @printf("\n     program killed...")
                    exit(1)
                end
            end
            ϵ[:,p] -= Δϵ
            epII[p] = ϵII 
        end
    end
end
