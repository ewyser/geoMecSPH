function plast!(τ,ϵ,epII,coh,phi,nmp,Del,Hp,cr)
    @threads for mp in 1:nmp
        c   = coh[mp]+Hp*epII[mp]
        if c<cr
            c = cr
        end
        ϕ  = phi[mp]
        σn = τ[:,mp]
        Δσ = σn[1]-σn[2]
        τt = sqrt(0.25*Δσ^2+σn[4]^2)
        σt = 0.5*(σn[1]+σn[2])
        f  = τt+σt*sin(ϕ)-c*cos(ϕ)
        
        if f>0.0
            if σt <= (c/tan(ϕ))
                β = abs(c*cos(ϕ)-sin(ϕ)*σt)/τt
                σn[1] = σt+β*Δσ/2
                σn[2] = σt-β*Δσ/2
                σn[4] = β*σn[4]
            else
                σn[1] = c/tan(ϕ)
                σn[2] = c/tan(ϕ)
                σn[4] = 0.0
            end
        end
        Δσ       = σn-τ[:,mp]
        τ[:,mp]  = σn
        ϵp       = Del\Δσ
        ϵ[:,mp]  = ϵ[:,mp]+ϵp
        epII[mp]+= sqrt(2/3*(ϵp[1]^2+ϵp[2]^2+ϵp[3]^2)+2*ϵp[4]^2) 
    end
end
